package com.zxin.mvc.region.data;

public class CountryGD {
	
	private int adcode;
	
	private String name;
	
	private double[] center;
	
	private int[] bbox;
	
	private int idealZoom;
	
	private String level;
	
	private int pz;
	
	private int raddix;
	
	private int padcode;

	public int getAdcode() {
		return adcode;
	}

	public void setAdcode(int adcode) {
		this.adcode = adcode;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public double[] getCenter() {
		return center;
	}

	public void setCenter(double[] center) {
		this.center = center;
	}

	public int[] getBbox() {
		return bbox;
	}

	public void setBbox(int[] bbox) {
		this.bbox = bbox;
	}

	public int getIdealZoom() {
		return idealZoom;
	}

	public void setIdealZoom(int idealZoom) {
		this.idealZoom = idealZoom;
	}

	public String getLevel() {
		return level;
	}

	public void setLevel(String level) {
		this.level = level;
	}

	public int getPz() {
		return pz;
	}

	public void setPz(int pz) {
		this.pz = pz;
	}

	public int getRaddix() {
		return raddix;
	}

	public void setRaddix(int raddix) {
		this.raddix = raddix;
	}

	public int getPadcode() {
		return padcode;
	}

	public void setPadcode(int padcode) {
		this.padcode = padcode;
	}
	
	
	
}
